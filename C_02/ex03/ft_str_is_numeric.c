int	ft_str_is_numeric(char *str)
{
	int	i;
	int	flag;

	flag = 1;
	i = 0;
	while (str[i] != 0)
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			flag = 0;
			break ;
		}
		i++;
	}
	if (flag == 0)
		return (0);
	else
		return (1);
}
