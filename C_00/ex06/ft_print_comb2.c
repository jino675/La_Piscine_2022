#include <unistd.h>

void	ft_print_comb2(void)
{
	char	front[2];
	char	end[2];
	char	f;
	char	e;

	f = 0;
	e = 1;
	while (f < 99)
	{
		while (e < 100)
		{
			front[0] = f / 10 + '0';
			front[1] = f % 10 + '0';
			end[0] = e / 10 + '0';
			end[1] = e % 10 + '0';
			write(1, front, 2);
			write(1, " ", 1);
			write(1, end, 2);
			if (!(f == 98 && e == 99))
				write(1, ", ", 2);
			e++;
		}
		f++;
		e = f + 1;
	}
}
